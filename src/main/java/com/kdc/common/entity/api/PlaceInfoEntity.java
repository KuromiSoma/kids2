package com.kdc.common.entity.api;

public class PlaceInfoEntity {

	private String placeId;
	private String placeName;
	private String placeTypeCd;
	private String latitude;
	private String longitude;
	private String radius;
	private String iconId;
	private String dispFlg;

	/**
	 * @return placeId
	 */
	public String getPlaceId() {
		return placeId;
	}

	/**
	 * @param placeId
	 *            セットする placeId
	 */
	public void setPlaceId(String placeId) {
		this.placeId = placeId;
	}

	/**
	 * @return placeName
	 */
	public String getPlaceName() {
		return placeName;
	}

	/**
	 * @param placeName
	 *            セットする placeName
	 */
	public void setPlaceName(String placeName) {
		this.placeName = placeName;
	}

	/**
	 * @return placeTypeCd
	 */
	public String getPlaceTypeCd() {
		return placeTypeCd;
	}

	/**
	 * @param placeTypeCd
	 *            セットする placeTypeCd
	 */
	public void setPlaceTypeCd(String placeTypeCd) {
		this.placeTypeCd = placeTypeCd;
	}

	/**
	 * @return latitude
	 */
	public String getLatitude() {
		return latitude;
	}

	/**
	 * @param latitude
	 *            セットする latitude
	 */
	public void setLatitude(String latitude) {
		this.latitude = latitude;
	}

	/**
	 * @return longitude
	 */
	public String getLongitude() {
		return longitude;
	}

	/**
	 * @param longitude
	 *            セットする longitude
	 */
	public void setLongitude(String longitude) {
		this.longitude = longitude;
	}

	/**
	 * @return radius
	 */
	public String getRadius() {
		return radius;
	}

	/**
	 * @param radius
	 *            セットする radius
	 */
	public void setRadius(String radius) {
		this.radius = radius;
	}

	/**
	 * @return iconId
	 */
	public String getIconId() {
		return iconId;
	}

	/**
	 * @param iconId
	 *            セットする iconId
	 */
	public void setIconId(String iconId) {
		this.iconId = iconId;
	}

	/**
	 * @return dispFlg
	 */
	public String getDispFlg() {
		return dispFlg;
	}

	/**
	 * @param dispFlg
	 *            セットする dispFlg
	 */
	public void setDispFlg(String dispFlg) {
		this.dispFlg = dispFlg;
	}

}
